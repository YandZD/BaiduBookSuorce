public class ChaptersBean {
    private int index;
    private String title;
    private String data_cid;
    private String data_href;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getData_cid() {
        return data_cid;
    }

    public void setData_cid(String data_cid) {
        this.data_cid = data_cid;
    }

    public String getData_href() {
        return data_href;
    }

    public void setData_href(String data_href) {
        this.data_href = data_href;
    }
}
